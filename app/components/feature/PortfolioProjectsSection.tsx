function enrichPortfolioHeadings() {
  const headingNodes = document.querySelectorAll("h2, h3");
  headingNodes.forEach((node) => {
    const source = node.textContent?.trim() ?? "";
    const replacement = HEADING_REPLACEMENTS[source];
    if (!replacement) return;
    if (node.textContent === replacement) return;
    node.textContent = replacement;
  });
}

function enrichGalleryCardDescriptions() {
  const heading = Array.from(document.querySelectorAll("h2")).find(
    (el) => el.textContent?.trim() === "Выполненные работы для промышленных предприятий"
  );
  if (!heading) return;

  const section = heading.closest("section");
  if (!section) return;

  const cards = section.querySelectorAll("button.group.relative.overflow-hidden");
  cards.forEach((card) => {
    const titleEl = card.querySelector("div.absolute.inset-x-0.bottom-0 p");
    if (!titleEl) return;

    const title = titleEl.textContent?.trim() ?? "";
    const description = CARD_DESCRIPTIONS[title];
    if (!description) return;

    const caption = titleEl.parentElement;
    if (!caption) return;

    let descEl = caption.querySelector<HTMLParagraphElement>(".ff-card-description");
    if (!descEl) {
      descEl = document.createElement("p");
      descEl.className =
        "ff-card-description mt-1 text-[11px] leading-[1.35] text-white/95 md:text-[12px]";
      caption.appendChild(descEl);
    }

    descEl.textContent = description;
  });
}

export default function PortfolioProjectsSection() {
  useEffect(() => {
    enrichPortfolioHeadings();
    enrichGalleryCardDescriptions();

    const observer = new MutationObserver(() => {
      enrichPortfolioHeadings();
      enrichGalleryCardDescriptions();
    });

    observer.observe(document.body, {
      subtree: true,
      childList: true,
    });

    return () => observer.disconnect();
  }, []);

  return <PortfolioPageBlocks />;
}
